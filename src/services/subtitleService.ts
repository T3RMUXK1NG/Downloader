export interface SubtitleOptions {
  url?: string;
  videoUrl?: string;
  languages?: string[];
  format?: SubtitleFormat;
  outputDir?: string;
  filename?: string;
  autoGenerated?: boolean;
  translateTo?: string;
  offset?: number;
  signal?: AbortSignal;
}

export interface SubtitleInfo {
  id: string;
  language: string;
  languageName: string;
  autoGenerated: boolean;
  format: string;
  url?: string;
  content?: string;
}

export interface SubtitleEntry {
  index: number;
  startTime: number;
  endTime: number;
  text: string;
}

export interface SubtitleData {
  id: string;
  language: string;
  format: SubtitleFormat;
  entries: SubtitleEntry[];
  metadata?: Record<string, string>;
}

export interface SubtitleProgress {
  subtitleId: string;
  status: SubtitleStatus;
  progress: number;
  message?: string;
  error?: string;
}

export interface SubtitleResult {
  success: boolean;
  subtitleId: string;
  filePath?: string;
  data?: SubtitleData;
  error?: string;
}

export enum SubtitleFormat {
  SRT = 'srt',
  VTT = 'vtt',
  ASS = 'ass',
  SSA = 'ssa',
  SUB = 'sub',
  TTML = 'ttml',
  JSON = 'json',
}

export enum SubtitleStatus {
  PENDING = 'pending',
  DOWNLOADING = 'downloading',
  CONVERTING = 'converting',
  TRANSLATING = 'translating',
  GENERATING = 'generating',
  COMPLETED = 'completed',
  FAILED = 'failed',
  CANCELLED = 'cancelled',
}

