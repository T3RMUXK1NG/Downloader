/**
 * Validate file path
 */
function isValidFilePath(path: string): boolean {
  if (path.includes('..')) return false;
  if (path.startsWith('/') && !path.startsWith('/downloads') && !path.startsWith('/tmp')) return false;
  return true;
}

/**
 * Validate subtitle request
 */
function validateSubtitleRequest(
  body: unknown,
  requestId: string,
  startTime: number
): { valid: boolean; error?: ReturnType<typeof validationError>; data?: SubtitleRequest } {
  if (!body || typeof body !== 'object') {
    return {
      valid: false,
      error: validationError('Request body is required', 'body', requestId, startTime),
    };
  }

  const request = body as Partial<SubtitleRequest>;

  // Operation validation
  if (!request.operation) {
    return {
      valid: false,
      error: validationError('Operation type is required', 'operation', requestId, startTime),
    };
  }

  if (!Object.values(SubtitleOperation).includes(request.operation)) {
    return {
      valid: false,
      error: validationError(
        `Invalid operation. Supported: ${Object.values(SubtitleOperation).join(', ')}`,
        'operation',
        requestId,
        startTime
      ),
    };
  }

  // Validate required fields based on operation
  switch (request.operation) {
    case SubtitleOperation.DOWNLOAD:
      if (!request.mediaPath) {
        return {
          valid: false,
          error: validationError('Media path is required for download operation', 'mediaPath', requestId, startTime),
        };
      }
      break;
    case SubtitleOperation.CONVERT:
      if (!request.subtitlePath) {
        return {
          valid: false,
          error: validationError('Subtitle path is required for convert operation', 'subtitlePath', requestId, startTime),
        };
      }
      break;
    case SubtitleOperation.EMBED:
      if (!request.mediaPath || !request.subtitlePath) {
        return {
          valid: false,
          error: validationError('Both media path and subtitle path are required for embed operation', 'mediaPath', requestId, startTime),
        };
      }
      break;
    case SubtitleOperation.EXTRACT:
      if (!request.mediaPath) {
        return {
          valid: false,
          error: validationError('Media path is required for extract operation', 'mediaPath', requestId, startTime),
        };
      }
      break;
    case SubtitleOperation.TRANSLATE:
      if (!request.subtitlePath || !request.targetLanguage) {
        return {
          valid: false,
          error: validationError('Subtitle path and target language are required for translate operation', 'subtitlePath', requestId, startTime),
        };
      }
      break;
    case SubtitleOperation.GENERATE:
      if (!request.mediaPath) {
        return {
          valid: false,
          error: validationError('Media path is required for generate operation', 'mediaPath', requestId, startTime),
        };
      }
      break;
    case SubtitleOperation.SYNC:
      if (!request.subtitlePath) {
        return {
          valid: false,
          error: validationError('Subtitle path is required for sync operation', 'subtitlePath', requestId, startTime),
        };
      }
      break;
  }

  // Validate paths
  if (request.mediaPath && !isValidUrl(request.mediaPath) && !isValidFilePath(request.mediaPath)) {
    return {
      valid: false,
      error: validationError('Invalid media path. Must be a valid URL or file path', 'mediaPath', requestId, startTime),
    };
  }

  if (request.subtitlePath && !isValidUrl(request.subtitlePath) && !isValidFilePath(request.subtitlePath)) {
    return {
      valid: false,
      error: validationError('Invalid subtitle path. Must be a valid URL or file path', 'subtitlePath', requestId, startTime),
    };
  }

  // Format validation
  if (request.outputFormat && !Object.values(SubtitleFormat).includes(request.outputFormat)) {
    return {
      valid: false,
      error: validationError(
        `Invalid output format. Supported: ${Object.values(SubtitleFormat).join(', ')}`,
        'outputFormat',
        requestId,
        startTime
      ),
    };
  }

  // Time offset validation
  if (request.timeOffset !== undefined && isNaN(request.timeOffset)) {
    return {
      valid: false,
      error: validationError('Time offset must be a valid number', 'timeOffset', requestId, startTime),
    };
  }

  // Webhook URL validation
  if (request.webhookUrl && !isValidUrl(request.webhookUrl)) {
    return {
      valid: false,
      error: validationError('Invalid webhook URL format', 'webhookUrl', requestId, startTime),
    };
  }

  return { valid: true, data: request as SubtitleRequest };
}

