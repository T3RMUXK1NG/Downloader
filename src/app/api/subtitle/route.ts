/**
 * Subtitle Manager
 * @description Manages all subtitle operations with queue support and progress tracking
 */
class SubtitleManager {
  private static instance: SubtitleManager;
  private operations: Map<string, SubtitleState> = new Map();
  private queue: string[] = [];
  private activeOperations = 0;
  private maxConcurrent = 5;

  private constructor() {}

  static getInstance(): SubtitleManager {
    if (!SubtitleManager.instance) {
      SubtitleManager.instance = new SubtitleManager();
    }
    return SubtitleManager.instance;
  }

  /**
   * Create a new subtitle operation
   */
  async createSubtitleOperation(
    request: SubtitleRequest,
    requestId: string
  ): Promise<{ subtitleId: string; wsChannel: string }> {
    const subtitleId = `sub_${Date.now().toString(36)}_${Math.random().toString(36).substr(2, 9)}`;
    const wsChannel = `subtitle_${subtitleId}`;

    const state: SubtitleState = {
      id: subtitleId,
      request,
      status: SubtitleStatus.PENDING,
      progress: 0,
      startTime: Date.now(),
      outputFiles: [],
      wsChannel,
    };

    this.operations.set(subtitleId, state);
    this.queue.push(subtitleId);

    logger.info(`Subtitle operation created: ${subtitleId}`, {
      operation: request.operation,
    }, requestId);

    this.processQueue();

    return { subtitleId, wsChannel };
  }

  /**
   * Get subtitle operation status
   */
  getSubtitleOperation(subtitleId: string): SubtitleState | undefined {
    return this.operations.get(subtitleId);
  }

  /**
   * Cancel a subtitle operation
   */
  cancelSubtitleOperation(subtitleId: string): boolean {
    const operation = this.operations.get(subtitleId);
    if (!operation) return false;

    operation.status = SubtitleStatus.CANCELLED;
    this.queue = this.queue.filter((id) => id !== subtitleId);

    logger.info(`Subtitle operation cancelled: ${subtitleId}`);
    return true;
  }

  /**
   * Get statistics
   */
  getStats(): { active: number; queued: number; total: number } {
    return {
      active: this.activeOperations,
      queued: this.queue.length,
      total: this.operations.size,
    };
  }

  /**
   * Process queue
   */
  private async processQueue(): Promise<void> {
    while (this.queue.length > 0 && this.activeOperations < this.maxConcurrent) {
      const subtitleId = this.queue.shift();
      if (!subtitleId) continue;

      const operation = this.operations.get(subtitleId);
      if (!operation || operation.status === SubtitleStatus.CANCELLED) continue;

      this.activeOperations++;
      operation.status = SubtitleStatus.PROCESSING;

      this.executeSubtitleOperation(subtitleId).finally(() => {
        this.activeOperations--;
        this.processQueue();
      });
    }
  }

  /**
   * Execute subtitle operation
   */
  private async executeSubtitleOperation(subtitleId: string): Promise<void> {
    const operation = this.operations.get(subtitleId);
    if (!operation) return;

    try {
      // Simulate subtitle operation progress
      for (let progress = 0; progress <= 100; progress += 5) {
        if (operation.status === SubtitleStatus.CANCELLED) return;

        operation.progress = progress;
        this.broadcastProgress(operation);
        await new Promise((resolve) => setTimeout(resolve, 200));
      }

      // Generate output files based on operation type
      const languages = operation.request.languages || [operation.request.language || 'en'];
      
      for (const lang of languages) {
        const langInfo = SUPPORTED_LANGUAGES.find(l => l.code === lang) || { code: lang, name: lang };
        const format = operation.request.outputFormat || SubtitleFormat.SRT;
        
        operation.outputFiles.push({
          language: lang,
          languageName: langInfo.name,
          format,
          path: `${operation.request.outputDir || '/downloads'}/${operation.request.outputFilename || 'subtitle'}_${lang}.${format}`,
          isAutoGenerated: operation.request.includeAutoGenerated,
        });
      }

      // Complete operation
      operation.status = SubtitleStatus.COMPLETED;
      operation.progress = 100;

      logger.info(`Subtitle operation completed: ${subtitleId}`, {
        operation: operation.request.operation,
        outputFiles: operation.outputFiles.length,
      });

      this.broadcastProgress(operation);

      // Send webhook if configured
      if (operation.request.webhookUrl) {
        await this.sendWebhook(operation.request.webhookUrl, {
          subtitleId,
          status: SubtitleStatus.COMPLETED,
          outputFiles: operation.outputFiles,
        });
      }
    } catch (error) {
      operation.status = SubtitleStatus.FAILED;
      operation.error = error instanceof Error ? error.message : 'Unknown error';

      logger.error(`Subtitle operation failed: ${subtitleId}`, { error: operation.error });
      this.broadcastProgress(operation);
    }
  }

  /**
   * Broadcast progress via WebSocket
   */
  private broadcastProgress(operation: SubtitleState): void {
    wsChannelManager.broadcast(operation.wsChannel, {
      type: operation.status === SubtitleStatus.COMPLETED
        ? WSMessageType.DOWNLOAD_COMPLETE
        : WSMessageType.DOWNLOAD_PROGRESS,
      data: {
        subtitleId: operation.id,
        progress: operation.progress,
        status: operation.status,
        operation: operation.request.operation,
      },
      timestamp: new Date().toISOString(),
      messageId: `msg_${Date.now()}`,
    });
  }

  /**
   * Send webhook notification
   */
  private async sendWebhook(url: string, data: unknown): Promise<void> {
    try {
      await fetch(url, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(data),
      });
    } catch (error) {
      logger.error(`Webhook failed: ${url}`, { error });
    }
  }
}

