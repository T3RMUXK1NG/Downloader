const middleware = createMiddleware(
  rateLimitMiddleware(RateLimitTier.BASIC),
  authMiddleware(AuthLevel.PUBLIC)
);

/**
 * POST /api/subtitle
 * @description Start a new subtitle operation
 */
export async function POST(request: NextRequest): Promise<Response> {
  const startTime = Date.now();
  const requestId = generateRequestId();

  const { response: middlewareResponse } = await middleware(request);
  if (middlewareResponse) return middlewareResponse;

  try {
    const body = await parseJsonBody<SubtitleRequest>(request);

    const validation = validateSubtitleRequest(body, requestId, startTime);
    if (!validation.valid || !validation.data) {
      return validation.error!;
    }

    const subtitleRequest = validation.data;

    // Set defaults
    subtitleRequest.outputDir = subtitleRequest.outputDir || '/downloads';
    subtitleRequest.outputFormat = subtitleRequest.outputFormat || SubtitleFormat.SRT;
    subtitleRequest.useWebSocket = subtitleRequest.useWebSocket ?? true;
    subtitleRequest.overwrite = subtitleRequest.overwrite ?? false;
    subtitleRequest.includeAutoGenerated = subtitleRequest.includeAutoGenerated ?? false;

    const { subtitleId, wsChannel } = await subtitleManager.createSubtitleOperation(subtitleRequest, requestId);

    const responseData: SubtitleResponse = {
      subtitleId,
      status: SubtitleStatus.PENDING,
      operation: subtitleRequest.operation,
      progress: 0,
      wsChannel: subtitleRequest.useWebSocket ? wsChannel : undefined,
    };

    logger.info(`Subtitle operation initiated successfully`, {
      subtitleId,
      operation: subtitleRequest.operation,
    }, requestId);

    return successResponse(responseData, requestId, startTime);
  } catch (error) {
    logger.error(`Subtitle request failed`, {
      error: error instanceof Error ? error.message : 'Unknown error',
    }, requestId);

    return errorResponse(
      {
        code: 'INTERNAL_ERROR',
        message: 'An unexpected error occurred while processing your subtitle request',
        suggestion: 'Please try again later or contact support if the issue persists',
      },
      requestId,
      startTime,
      500
    );
  }
}

/**
 * GET /api/subtitle
 * @description Get subtitle operation status
 */
export async function GET(request: NextRequest): Promise<Response> {
  const startTime = Date.now();
  const requestId = generateRequestId();

  const { response: middlewareResponse } = await middleware(request);
  if (middlewareResponse) return middlewareResponse;

  try {
    const { searchParams } = new URL(request.url);
    const subtitleId = searchParams.get('id');
    const action = searchParams.get('action');

    if (subtitleId) {
      const operation = subtitleManager.getSubtitleOperation(subtitleId);

      if (!operation) {
        return errorResponse(
          {
            code: 'NOT_FOUND',
            message: `Subtitle operation not found: ${subtitleId}`,
            suggestion: 'Please check the subtitle ID and try again',
          },
          requestId,
          startTime,
          404
        );
      }

      if (action === 'cancel') {
        const cancelled = subtitleManager.cancelSubtitleOperation(subtitleId);
        return successResponse(
          {
            subtitleId,
            status: cancelled ? SubtitleStatus.CANCELLED : operation.status,
            message: cancelled ? 'Subtitle operation cancelled successfully' : 'Failed to cancel subtitle operation',
          },
          requestId,
          startTime
        );
      }

      const responseData: SubtitleResponse = {
        subtitleId: operation.id,
        status: operation.status,
        operation: operation.request.operation,
        progress: operation.progress,
        outputFiles: operation.outputFiles,
        error: operation.error,
      };

      return successResponse(responseData, requestId, startTime);
    }

    const stats = subtitleManager.getStats();
    return successResponse(
      {
        stats,
        supportedLanguages: SUPPORTED_LANGUAGES,
        supportedFormats: Object.values(SubtitleFormat),
        operations: Object.values(SubtitleOperation),
        message: 'Subtitle API v3.0.1 ULTIMATE NEXUS',
      },
      requestId,
      startTime
    );
  } catch (error) {
    logger.error(`Subtitle status request failed`, {
      error: error instanceof Error ? error.message : 'Unknown error',
    }, requestId);

    return errorResponse(
      {
        code: 'INTERNAL_ERROR',
        message: 'Failed to retrieve subtitle operation status',
      },
      requestId,
      startTime,
      500
    );
  }
}

/**
 * DELETE /api/subtitle
 * @description Cancel a subtitle operation
 */
export async function DELETE(request: NextRequest): Promise<Response> {
  const startTime = Date.now();
  const requestId = generateRequestId();

  const { response: middlewareResponse } = await middleware(request);
  if (middlewareResponse) return middlewareResponse;

  try {
    const { searchParams } = new URL(request.url);
    const subtitleId = searchParams.get('id');

    if (!subtitleId) {
      return validationError('Subtitle ID is required', 'id', requestId, startTime);
    }

    const cancelled = subtitleManager.cancelSubtitleOperation(subtitleId);

    if (!cancelled) {
      return errorResponse(
        {
          code: 'NOT_FOUND',
          message: `Subtitle operation not found or already completed: ${subtitleId}`,
        },
        requestId,
        startTime,
        404
      );
    }

    return successResponse(
      {
        subtitleId,
        status: SubtitleStatus.CANCELLED,
        message: 'Subtitle operation cancelled successfully',
      },
      requestId,
      startTime
    );
  } catch (error) {
    logger.error(`Subtitle cancellation failed`, {
      error: error instanceof Error ? error.message : 'Unknown error',
    }, requestId);

    return errorResponse(
      {
        code: 'INTERNAL_ERROR',
        message: 'Failed to cancel subtitle operation',
      },
      requestId,
      startTime,
      500
    );
  }
}

