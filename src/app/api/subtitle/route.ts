/**
 * Subtitle operation status enumeration
 */
export enum SubtitleStatus {
  PENDING = 'pending',
  PROCESSING = 'processing',
  COMPLETED = 'completed',
  FAILED = 'failed',
  CANCELLED = 'cancelled',
}

/**
 * Subtitle operation type enumeration
 */
export enum SubtitleOperation {
  DOWNLOAD = 'download',
  CONVERT = 'convert',
  EMBED = 'embed',
  EXTRACT = 'extract',
  TRANSLATE = 'translate',
  GENERATE = 'generate',
  SYNC = 'sync',
}

/**
 * Subtitle format enumeration
 */
export enum SubtitleFormat {
  SRT = 'srt',
  VTT = 'vtt',
  ASS = 'ass',
  SSA = 'ssa',
  SUB = 'sub',
  SBV = 'sbv',
  TTML = 'ttml',
  JSON = 'json',
}

/**
 * Subtitle request interface
 */
export interface SubtitleRequest {
  /** Operation type */
  operation: SubtitleOperation;
  /** Video/media URL or file path */
  mediaPath?: string;
  /** Subtitle file URL or path (for embed/convert) */
  subtitlePath?: string;
  /** Output directory */
  outputDir?: string;
  /** Output filename */
  outputFilename?: string;
  /** Output format */
  outputFormat?: SubtitleFormat;
  /** Language code (e.g., 'en', 'es', 'fr') */
  language?: string;
  /** Target language for translation */
  targetLanguage?: string;
  /** List of languages to download */
  languages?: string[];
  /** Include auto-generated subtitles */
  includeAutoGenerated?: boolean;
  /** Time offset in seconds (for sync) */
  timeOffset?: number;
  /** Whether to embed in video */
  embedInVideo?: boolean;
  /** Video output path (for embed) */
  videoOutputPath?: string;
  /** Burn subtitles into video */
  burnIntoVideo?: boolean;
  /** Subtitle styling (for ASS/SSA) */
  styling?: SubtitleStyling;
  /** Use WebSocket for progress updates */
  useWebSocket?: boolean;
  /** Webhook URL for completion notification */
  webhookUrl?: string;
  /** Overwrite existing files */
  overwrite?: boolean;
}

