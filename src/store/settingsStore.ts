const generateId = (): string => `profile_${Date.now()}_${Math.random().toString(36).substring(2, 10)}`;

const createUndoSnapshot = (settings: Partial<AppSettings>, action: string): UndoState => ({
  settings: JSON.parse(JSON.stringify(settings)),
  timestamp: Date.now(),
  action,
});

const deepClone = <T>(obj: T): T => JSON.parse(JSON.stringify(obj));

// ═══════════════════════════════════════════════════════════════════════════════
// STORE IMPLEMENTATION
// ═══════════════════════════════════════════════════════════════════════════════

export const useSettingsStore = create<SettingsStore>()(
  persist(
    immer((set, get) => ({
      // ═════════════════════════════════════════════════════════════════════════
      // INITIAL STATE
      // ═════════════════════════════════════════════════════════════════════════
      settings: deepClone(DEFAULT_APP_SETTINGS),
      profiles: [],
      activeProfileId: null,
      isDirty: false,
      lastSaved: null,
      lastModified: null,
      undoStack: [],
      redoStack: [],
      maxUndoSteps: 50,
      isHydrated: false,

      // ═════════════════════════════════════════════════════════════════════════
      // SETTINGS ACTIONS
      // ═════════════════════════════════════════════════════════════════════════

      setSettings: (settings) => {
        set((state) => {
          state.undoStack.push(createUndoSnapshot(state.settings, 'setSettings'));
          if (state.undoStack.length > state.maxUndoSteps) {
            state.undoStack.shift();
          }
          state.redoStack = [];
          
          state.settings = settings;
          state.isDirty = true;
          state.lastModified = new Date();
        });
      },

      updateSettings: (category, updates) => {
        set((state) => {
          state.undoStack.push(createUndoSnapshot({ [category]: state.settings[category] }, `update ${String(category)}`));
          if (state.undoStack.length > state.maxUndoSteps) {
            state.undoStack.shift();
          }
          state.redoStack = [];
          
          state.settings[category] = {
            ...state.settings[category],
            ...updates,
          } as AppSettings[typeof category];
          state.isDirty = true;
          state.lastModified = new Date();
        });
      },

      updateNestedSetting: (path, value) => {
        set((state) => {
          const keys = path.split('.');
          let current: Record<string, unknown> = state.settings;
          
          for (let i = 0; i < keys.length - 1; i++) {
            current = current[keys[i]] as Record<string, unknown>;
          }
          
          current[keys[keys.length - 1]] = value;
          state.isDirty = true;
          state.lastModified = new Date();
        });
      },

      resetToDefaults: () => {
        set((state) => {
          state.undoStack.push(createUndoSnapshot(state.settings, 'resetToDefaults'));
          state.redoStack = [];
          
          state.settings = deepClone(DEFAULT_APP_SETTINGS);
          state.isDirty = true;
          state.lastModified = new Date();
        });
      },

      resetCategory: (category) => {
        set((state) => {
          state.undoStack.push(createUndoSnapshot({ [category]: state.settings[category] }, `reset ${String(category)}`));
          state.redoStack = [];
          
          state.settings[category] = deepClone(DEFAULT_APP_SETTINGS[category]);
          state.isDirty = true;
          state.lastModified = new Date();
        });
      },

      // ═════════════════════════════════════════════════════════════════════════
      // PROFILE ACTIONS
      // ═════════════════════════════════════════════════════════════════════════

      createProfile: (name, description) => {
        const id = generateId();
        set((state) => {
          state.profiles.push({
            id,
            name,
            description,
            settings: deepClone(state.settings),
            createdAt: new Date(),
            updatedAt: new Date(),
            isDefault: false,
          });
        });
        return id;
      },

      updateProfile: (id, updates) => {
        set((state) => {
          const index = state.profiles.findIndex((p) => p.id === id);
          if (index !== -1) {
            state.profiles[index] = {
              ...state.profiles[index],
              ...updates,
              updatedAt: new Date(),
            };
          }
        });
      },

      deleteProfile: (id) => {
        set((state) => {
          const profile = state.profiles.find((p) => p.id === id);
          if (profile && !profile.isDefault) {
            state.profiles = state.profiles.filter((p) => p.id !== id);
            if (state.activeProfileId === id) {
              state.activeProfileId = null;
            }
          }
        });
      },

      activateProfile: (id) => {
        set((state) => {
          const profile = state.profiles.find((p) => p.id === id);
          if (profile) {
            state.undoStack.push(createUndoSnapshot(state.settings, `activate profile: ${profile.name}`));
            state.redoStack = [];
            
            state.settings = { ...deepClone(DEFAULT_APP_SETTINGS), ...profile.settings };
            state.activeProfileId = id;
            state.isDirty = true;
          }
        });
      },

      duplicateProfile: (id) => {
        const newId = generateId();
        set((state) => {
          const profile = state.profiles.find((p) => p.id === id);
          if (profile) {
            state.profiles.push({
              ...profile,
              id: newId,
              name: `${profile.name} (Copy)`,
              createdAt: new Date(),
              updatedAt: new Date(),
              isDefault: false,
            });
          }
        });
        return newId;
      },

      exportProfile: (id) => {
        const profile = get().profiles.find((p) => p.id === id);
        if (profile) {
          return JSON.stringify(profile, null, 2);
        }
        return '';
      },

      importProfile: (data) => {
        try {
          const profile = JSON.parse(data) as SettingsProfile;
          const id = generateId();
          set((state) => {
            state.profiles.push({
              ...profile,
              id,
              createdAt: new Date(),
              updatedAt: new Date(),
              isDefault: false,
            });
          });
          return id;
        } catch {
          return null;
        }
      },

      // ═════════════════════════════════════════════════════════════════════════
      // VALIDATION
      // ═════════════════════════════════════════════════════════════════════════

      validateSettings: () => {
        const state = get();
        const errors: ValidationResult['errors'] = [];
        const warnings: ValidationResult['warnings'] = [];

        // Validate download settings
        if (state.settings.download.maxConcurrentDownloads < 1 || state.settings.download.maxConcurrentDownloads > 10) {
          errors.push({ path: 'download.maxConcurrentDownloads', message: 'Must be between 1 and 10' });
        }
        if (state.settings.download.timeout < 10) {
          warnings.push({ path: 'download.timeout', message: 'Very short timeout may cause failures' });
        }

        // Validate network settings
        if (state.settings.network.useProxy && !state.settings.network.proxy) {
          errors.push({ path: 'network.proxy', message: 'Proxy URL is required when proxy is enabled' });
        }

        // Validate theme settings
        if (state.settings.theme.fontScale < 0.5 || state.settings.theme.fontScale > 2) {
          errors.push({ path: 'theme.fontScale', message: 'Must be between 0.5 and 2' });
        }

        return { valid: errors.length === 0, errors, warnings };
      },

      validateCategory: (category) => {
        const state = get();
        const errors: ValidationResult['errors'] = [];
        const warnings: ValidationResult['warnings'] = [];

        switch (category) {
          case 'download':
            if (state.settings.download.maxConcurrentDownloads < 1 || state.settings.download.maxConcurrentDownloads > 10) {
              errors.push({ path: 'download.maxConcurrentDownloads', message: 'Must be between 1 and 10' });
            }
            break;
          case 'network':
            if (state.settings.network.useProxy && !state.settings.network.proxy) {
              errors.push({ path: 'network.proxy', message: 'Proxy URL is required when proxy is enabled' });
            }
            break;
          case 'theme':
            if (state.settings.theme.fontScale < 0.5 || state.settings.theme.fontScale > 2) {
              errors.push({ path: 'theme.fontScale', message: 'Must be between 0.5 and 2' });
            }
            break;
        }

        return { valid: errors.length === 0, errors, warnings };
      },

      // ═════════════════════════════════════════════════════════════════════════
      // KEYBOARD SHORTCUTS
      // ═════════════════════════════════════════════════════════════════════════

      registerShortcut: (shortcut) => {
        const id = `shortcut_${Date.now()}`;
        set((state) => {
          state.settings.shortcuts.shortcuts.push({ ...shortcut, id });
        });
        return id;
      },

      updateShortcut: (id, updates) => {
        set((state) => {
          const index = state.settings.shortcuts.shortcuts.findIndex((s) => s.id === id);
          if (index !== -1) {
            state.settings.shortcuts.shortcuts[index] = {
              ...state.settings.shortcuts.shortcuts[index],
              ...updates,
            };
          }
        });
      },

      removeShortcut: (id) => {
        set((state) => {
          state.settings.shortcuts.shortcuts = state.settings.shortcuts.shortcuts.filter((s) => s.id !== id);
        });
      },

      resetShortcuts: () => {
        set((state) => {
          state.settings.shortcuts.shortcuts = deepClone(DEFAULT_APP_SETTINGS.shortcuts.shortcuts);
        });
      },

      getShortcutByKey: (key, modifiers) => {
        const state = get();
        const sortedModifiers = [...modifiers].sort();
        return state.settings.shortcuts.shortcuts.find((s) => {
          const shortcutModifiers = [...s.modifiers].sort();
          return s.key.toLowerCase() === key.toLowerCase() &&
            JSON.stringify(shortcutModifiers) === JSON.stringify(sortedModifiers);
        });
      },

      // ═════════════════════════════════════════════════════════════════════════
      // THEME
      // ═════════════════════════════════════════════════════════════════════════

      setThemeMode: (mode) => {
        set((state) => {
          state.settings.theme.mode = mode;
          state.isDirty = true;
        });
      },

      setAccentColor: (color) => {
        set((state) => {
          state.settings.theme.accentColor = color;
          state.settings.theme.customColors.primary = color;
          state.isDirty = true;
        });
      },

      setCustomColor: (name, color) => {
        set((state) => {
          state.settings.theme.customColors[name] = color;
          state.isDirty = true;
        });
      },

      resetTheme: () => {
        set((state) => {
          state.settings.theme = deepClone(DEFAULT_APP_SETTINGS.theme);
          state.isDirty = true;
        });
      },

      exportTheme: () => {
        return JSON.stringify(get().settings.theme, null, 2);
      },

      importTheme: (data) => {
        try {
          const theme = JSON.parse(data) as ThemeSettings;
          set((state) => {
            state.settings.theme = theme;
            state.isDirty = true;
          });
          return true;
        } catch {
          return false;
        }
      },

      // ═════════════════════════════════════════════════════════════════════════
      // NOTIFICATIONS
      // ═════════════════════════════════════════════════════════════════════════

      updateNotificationSettings: (updates) => {
        set((state) => {
          state.settings.notifications = {
            ...state.settings.notifications,
            ...updates,
          };
          state.isDirty = true;
        });
      },

      testNotification: () => {
        if (typeof window !== 'undefined' && 'Notification' in window) {
          if (Notification.permission === 'granted') {
            new Notification('RS Toolkit Test', {
              body: 'Notification settings are working correctly!',
              icon: '/favicon.ico',
            });
          } else if (Notification.permission !== 'denied') {
            Notification.requestPermission().then((permission) => {
              if (permission === 'granted') {
                new Notification('RS Toolkit Test', {
                  body: 'Notification settings are working correctly!',
                  icon: '/favicon.ico',
                });
              }
            });
          }
        }
      },

      // ═════════════════════════════════════════════════════════════════════════
      // PERSISTENCE
      // ═════════════════════════════════════════════════════════════════════════

      markSaved: () => {
        set((state) => {
          state.isDirty = false;
          state.lastSaved = new Date();
        });
      },

      saveToStorage: () => {
        set((state) => {
          state.lastSaved = new Date();
        });
      },

      loadFromStorage: () => {
        set((state) => {
          state.isHydrated = true;
        });
      },

      exportSettings: () => {
        return JSON.stringify({
          settings: get().settings,
          profiles: get().profiles,
          exportedAt: new Date().toISOString(),
          version: '3.2.0',
        }, null, 2);
      },

      importSettings: (data) => {
        try {
          const parsed = JSON.parse(data);
          set((state) => {
            if (parsed.settings) {
              state.settings = { ...deepClone(DEFAULT_APP_SETTINGS), ...parsed.settings };
            }
            if (parsed.profiles) {
              state.profiles = parsed.profiles;
            }
            state.isDirty = true;
          });
          return true;
        } catch {
          return false;
        }
      },

      // ═════════════════════════════════════════════════════════════════════════
      // UNDO/REDO
      // ═════════════════════════════════════════════════════════════════════════

      undo: () => {
        const state = get();
        if (state.undoStack.length === 0) return false;

        const previousState = state.undoStack[state.undoStack.length - 1];
        
        set((s) => {
          s.redoStack.push(createUndoSnapshot(s.settings, 'redo'));
          if (s.redoStack.length > s.maxUndoSteps) {
            s.redoStack.shift();
          }
          
          s.settings = { ...s.settings, ...previousState.settings };
          s.undoStack.pop();
        });

        return true;
      },

      redo: () => {
        const state = get();
        if (state.redoStack.length === 0) return false;

        const nextState = state.redoStack[state.redoStack.length - 1];
        
        set((s) => {
          s.undoStack.push(createUndoSnapshot(s.settings, 'undo'));
          if (s.undoStack.length > s.maxUndoSteps) {
            s.undoStack.shift();
          }
          
          s.settings = { ...s.settings, ...nextState.settings };
          s.redoStack.pop();
        });

        return true;
      },

      canUndo: () => get().undoStack.length > 0,
      canRedo: () => get().redoStack.length > 0,

      clearHistory: () => {
        set((state) => {
          state.undoStack = [];
          state.redoStack = [];
        });
      },

      // ═════════════════════════════════════════════════════════════════════════
      // HYDRATION
      // ═════════════════════════════════════════════════════════════════════════

      setHydrated: (hydrated) => {
        set((state) => {
          state.isHydrated = hydrated;
        });
      },
    })),
    {
      name: 'rs-toolkit-settings',
      storage: createJSONStorage(() => localStorage),
      partialize: (state) => ({
        settings: state.settings,
        profiles: state.profiles,
        activeProfileId: state.activeProfileId,
      }),
      onRehydrateStorage: () => (state) => {
        if (state) {
          state.isHydrated = true;
          state.lastSaved = new Date();
        }
      },
    }
  )
);

