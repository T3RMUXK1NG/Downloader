/**
 * Settings profile
 */
export interface SettingsProfile {
  id: string;
  name: string;
  description?: string;
  settings: Partial<AppSettings>;
  createdAt: Date;
  updatedAt: Date;
  isDefault: boolean;
}

/**
 * Settings validation result
 */
export interface ValidationResult {
  valid: boolean;
  errors: { path: string; message: string }[];
  warnings: { path: string; message: string }[];
}

/**
 * Undo/redo state
 */
interface UndoState {
  settings: Partial<AppSettings>;
  timestamp: number;
  action: string;
}

/**
 * Store state interface
 */
export interface SettingsStoreState {
  settings: AppSettings;
  profiles: SettingsProfile[];
  activeProfileId: string | null;
  isDirty: boolean;
  lastSaved: Date | null;
  lastModified: Date | null;
  undoStack: UndoState[];
  redoStack: UndoState[];
  maxUndoSteps: number;
  isHydrated: boolean;
}

/**
 * Store actions interface
 */
export interface SettingsStoreActions {
  // Settings actions
  setSettings: (settings: AppSettings) => void;
  updateSettings: <K extends keyof AppSettings>(
    category: K,
    updates: Partial<AppSettings[K]>
  ) => void;
  updateNestedSetting: (path: string, value: unknown) => void;
  resetToDefaults: () => void;
  resetCategory: <K extends keyof AppSettings>(category: K) => void;
  
  // Profile actions
  createProfile: (name: string, description?: string) => string;
  updateProfile: (id: string, updates: Partial<SettingsProfile>) => void;
  deleteProfile: (id: string) => void;
  activateProfile: (id: string) => void;
  duplicateProfile: (id: string) => string;
  exportProfile: (id: string) => string;
  importProfile: (data: string) => string | null;
  
  // Validation
  validateSettings: () => ValidationResult;
  validateCategory: <K extends keyof AppSettings>(category: K) => ValidationResult;
  
  // Keyboard shortcuts
  registerShortcut: (shortcut: Omit<KeyboardShortcut, 'id'>) => string;
  updateShortcut: (id: string, updates: Partial<KeyboardShortcut>) => void;
  removeShortcut: (id: string) => void;
  resetShortcuts: () => void;
  getShortcutByKey: (key: string, modifiers: ('ctrl' | 'alt' | 'shift' | 'meta')[]) => KeyboardShortcut | undefined;
  
  // Theme
  setThemeMode: (mode: 'light' | 'dark' | 'system') => void;
  setAccentColor: (color: string) => void;
  setCustomColor: (name: keyof ThemeSettings['customColors'], color: string) => void;
  resetTheme: () => void;
  exportTheme: () => string;
  importTheme: (data: string) => boolean;
  
  // Notifications
  updateNotificationSettings: (updates: Partial<NotificationSettings>) => void;
  testNotification: () => void;
  
  // Persistence
  markSaved: () => void;
  saveToStorage: () => void;
  loadFromStorage: () => void;
  exportSettings: () => string;
  importSettings: (data: string) => boolean;
  
  // Undo/Redo
  undo: () => boolean;
  redo: () => boolean;
  canUndo: () => boolean;
  canRedo: () => boolean;
  clearHistory: () => void;
  
  // Hydration
  setHydrated: (hydrated: boolean) => void;
}

/**
 * Combined store type
 */
export type SettingsStore = SettingsStoreState & SettingsStoreActions;

